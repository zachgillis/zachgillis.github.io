function getTodaysClue() {
    const today = new Date().toISOString().split('T')[0]; // Format: YYYY-MM-DD
    console.log("Today's Date:", today); // Log today's date

    // Log available dates in cluesData for debugging
    console.log("Available dates in cluesData:", cluesData.clues.map(clue => clue.date));

    return cluesData.clues.find(clue => clue.date === today);
}

let currentWord;
let clues;
let todaysClue = getTodaysClue();
if (todaysClue) {
    currentWord = todaysClue.word;
    clues = todaysClue.details;
} else {
    console.error("No clue for today's date in cluesData.");
    // Provide a default word and clues
    currentWord = 'apple'; // Example default word
    clues = {
        "category": "frduit",
        "color": "red or green",
        "size": "small",
        "usage": "edible, often eaten raw"
    };
}

let cluesGiven = Array(4).fill({ category: '', clue: '' });
let guessCount = 0;
let isCorrectAnswer = false;

function showClue() {
    let filledCluesCount = cluesGiven.filter(clue => clue.category !== '').length;
    if (filledCluesCount < Object.keys(clues).length) {
        let clueKey = Object.keys(clues)[filledCluesCount];
        cluesGiven[filledCluesCount] = { category: clueKey, clue: clues[clueKey] };
        updateClueDisplay();
    }
}

function updateClueDisplay() {
    let clueContainer = document.getElementById('clue-container');
    clueContainer.innerHTML = cluesGiven.map(clueInfo => {
        return `<div class="clue-column">
                    <div class="clue-category">${clueInfo.category}</div>
                    <div class="clue-text">${clueInfo.clue}</div>
                </div>`;
    }).join('');
}

function submitGuess() {
    let guessInput = document.getElementById('guess');
    let guess = guessInput.value.toLowerCase().trim();

    if (isCorrectAnswer || !guess) {
        return; // If the correct answer has already been guessed, or input is empty, don't do anything
    }

    let guessCounterDiv = document.getElementById('guess-counter');
    guessCount++; // Increment the guess count for every guess, including the correct one

    if (guess === currentWord.toLowerCase()) { // Compare with currentWord in lowercase
        isCorrectAnswer = true;
        document.getElementById('result').innerText = 'Correct! Well done!';
        guessInput.disabled = true; // Disable the input field
        confetti({
            particleCount: 100,
            spread: 70,
            origin: { y: 0.6 },
        });
    } else {
        guessInput.classList.add('shake');
        setTimeout(() => guessInput.classList.remove('shake'), 500);
        if (cluesGiven[cluesGiven.length - 1].category === '') {
            showClue();
        }
        guessInput.value = ''; // Clear the input field for the next guess
    }

    guessCounterDiv.innerHTML = `Guesses so far: <span class="guess-number">${guessCount}</span>`;
}

function handleKeyPress(event) {
    if (event.key === 'Enter') {
        submitGuess();
    }
}

document.getElementById('guess').addEventListener('keypress', handleKeyPress);

// Initialize the first clue
if (!isCorrectAnswer) {
    showClue();
}